package in.co.javacoder.strings;

public class EqualsIgnoreCasesMethod {

	public static void main(String[] args) {
		String wordOne = "Anees Fathima";
		String wordTwo = "Abdhiya";
		System.out.println(wordOne.equalsIgnoreCase(wordTwo));
		System.out.println(wordTwo.equalsIgnoreCase("Abdhiya"));

	}

}
