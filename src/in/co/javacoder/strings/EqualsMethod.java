package in.co.javacoder.strings;

public class EqualsMethod {

	public static void main(String[] args) {
		String wordOne ="anees";
		String wordTwo ="arif";
		System.out.println(wordOne.equals(wordTwo));
		System.out.println(wordOne.equals("anees"));
		System.out.println(wordTwo.equals("arif"));
		System.out.println(wordOne.equals("Anees"));
	}

}
